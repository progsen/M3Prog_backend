<?php

$het_regent = true;
$zon_schijnt = false;

// Booleans gebruik je in een if om beslissing te maken
// Is het true dan wordt de code in de if uitgevoerd
if ($het_regent) {
  echo "Het regent helaas. ";
}

if ($zon_schijnt) {
  echo "Maar de zon schijnt wel!";
} else {
  // Is het false dan wordt de code in de else uitgevoerd
  // Als er een else is...
  echo "En er is ook geen zon :-(";
}
